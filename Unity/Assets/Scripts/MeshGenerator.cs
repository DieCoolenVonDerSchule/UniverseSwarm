    void createShapeCubeMarching()
    {
        for(int i = 0; i<densityMap.GetLength(0)-1; i++)
        {
            for(int j = 0; j<densityMap.GetLength(1)-1; j++)
            {
                for(int k = 0; k<densityMap.GetLength(2)-1; k++)
                {
                    calculateTrianglesFromCube(i, j, k);
                }
            }
        }

        for(int i = 0; i<verts.Count; i++)
        {
            verts[i] *= 20f;
        }

        print("Tris: " + tris.Count);
    }

    void calculateTrianglesFromCube(int cornerX, int cornerY, int cornerZ)
    {
        Vector3Int coord = new Vector3Int(cornerX, cornerY, cornerZ);

        Vector3Int[] cornerCoords = new Vector3Int[8];
        cornerCoords[0] = coord + new Vector3Int(0, 0, 0);
        cornerCoords[1] = coord + new Vector3Int(1, 0, 0);
        cornerCoords[2] = coord + new Vector3Int(1, 0, 1);
        cornerCoords[3] = coord + new Vector3Int(0, 0, 1);
        cornerCoords[4] = coord + new Vector3Int(0, 1, 0);
        cornerCoords[5] = coord + new Vector3Int(1, 1, 0);
        cornerCoords[6] = coord + new Vector3Int(1, 1, 1);
        cornerCoords[7] = coord + new Vector3Int(0, 1, 1);

        int cubeConfiguration = 0;
        for (int i = 0; i < 8; i++)
        {
            if (densityMap[cornerCoords[i].x, cornerCoords[i].y, cornerCoords[i].z] > 0.7f)
            {
                cubeConfiguration |= (1 << i);
            }
        }

        int[] edgeIndices = new int[16];

        for (int i = 0; i<triangulation.GetLength(1); i++)
        {
            edgeIndices[i] = triangulation[cubeConfiguration, i];
        }

        Vector3 center = new Vector3(densityMap.GetLength(0) / 2f, densityMap.GetLength(1) / 2f, densityMap.GetLength(2) / 2f);
        float maxDistance = Vector3.Distance(center, new Vector3(0f, 0f, 0f));

        for (int i = 0; i < 16; i += 3)
        {
            // If edge index is -1, then no further vertices exist in this configuration
            if (edgeIndices[i] == -1) { break; }

            // Get indices of the two corner points defining the edge that the surface passes through.
            // (Do this for each of the three edges we're currently looking at).
            int edgeIndexA = edgeIndices[i];
            int a0 = cornerIndexAFromEdge[edgeIndexA];
            int a1 = cornerIndexBFromEdge[edgeIndexA];

            int edgeIndexB = edgeIndices[i + 1];
            int b0 = cornerIndexAFromEdge[edgeIndexB];
            int b1 = cornerIndexBFromEdge[edgeIndexB];

            int edgeIndexC = edgeIndices[i + 2];
            int c0 = cornerIndexAFromEdge[edgeIndexC];
            int c1 = cornerIndexBFromEdge[edgeIndexC];

            //Vector3 vertexA = (cornerCoords[a0] + cornerCoords[a1]) / 2 * 20;
            //Vector3 vertexB = (cornerCoords[b0] + cornerCoords[b1]) / 2 * 20;
            //Vector3 vertexC = (cornerCoords[c0] + cornerCoords[c1]) / 2 * 20;

            
            Vector3 vertexA = cornerCoords[a0] + ((isoLevel - densityMap[cornerCoords[a0].x, cornerCoords[a0].y, cornerCoords[a0].z]) / (densityMap[cornerCoords[a1].x, cornerCoords[a1].y, cornerCoords[a1].z] - densityMap[cornerCoords[a0].x, cornerCoords[a0].y, cornerCoords[a0].z])) * (Vector3)(cornerCoords[a1] - cornerCoords[a0]);
            Vector3 vertexB = cornerCoords[b0] + ((isoLevel - densityMap[cornerCoords[b0].x, cornerCoords[b0].y, cornerCoords[b0].z]) / (densityMap[cornerCoords[b1].x, cornerCoords[b1].y, cornerCoords[b1].z] - densityMap[cornerCoords[b0].x, cornerCoords[b0].y, cornerCoords[b0].z])) * (Vector3)(cornerCoords[b1] - cornerCoords[b0]);
            Vector3 vertexC = cornerCoords[c0] + ((isoLevel - densityMap[cornerCoords[c0].x, cornerCoords[c0].y, cornerCoords[c0].z]) / (densityMap[cornerCoords[c1].x, cornerCoords[c1].y, cornerCoords[c1].z] - densityMap[cornerCoords[c0].x, cornerCoords[c0].y, cornerCoords[c0].z])) * (Vector3)(cornerCoords[c1] - cornerCoords[c0]);

            if (verts.Contains(vertexA)) tris.Add(verts.IndexOf(vertexA));
            else
            {
                verts.Add(vertexA);
                colors.Add(gradient.Evaluate(Vector3.Distance(vertexA, center)/maxDistance));
                tris.Add(verts.IndexOf(vertexA));
            }

            if (verts.Contains(vertexB)) tris.Add(verts.IndexOf(vertexB));
            else
            {
                verts.Add(vertexB);
                colors.Add(gradient.Evaluate(Vector3.Distance(vertexB, center) / maxDistance));
                tris.Add(verts.IndexOf(vertexB));

            }

            if (verts.Contains(vertexC)) tris.Add(verts.IndexOf(vertexC));
            else
            {
                verts.Add(vertexC);
                colors.Add(gradient.Evaluate(Vector3.Distance(vertexC, center) / maxDistance));
                tris.Add(verts.IndexOf(vertexC));
            }
        }
    }

